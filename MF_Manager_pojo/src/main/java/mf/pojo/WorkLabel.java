package mf.pojo;

public class WorkLabel {
    private Long workLabelId;

    private Long workId;

    private Long labelId;

    public Long getWorkLabelId() {
        return workLabelId;
    }

    public void setWorkLabelId(Long workLabelId) {
        this.workLabelId = workLabelId;
    }

    public Long getWorkId() {
        return workId;
    }

    public void setWorkId(Long workId) {
        this.workId = workId;
    }

    public Long getLabelId() {
        return labelId;
    }

    public void setLabelId(Long labelId) {
        this.labelId = labelId;
    }
}