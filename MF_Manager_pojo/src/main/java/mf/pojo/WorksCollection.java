package mf.pojo;

public class WorksCollection {
    private Long worksCollectionId;

    private Long workId;

    private Long worksId;

    public Long getWorksCollectionId() {
        return worksCollectionId;
    }

    public void setWorksCollectionId(Long worksCollectionId) {
        this.worksCollectionId = worksCollectionId;
    }

    public Long getWorkId() {
        return workId;
    }

    public void setWorkId(Long workId) {
        this.workId = workId;
    }

    public Long getWorksId() {
        return worksId;
    }

    public void setWorksId(Long worksId) {
        this.worksId = worksId;
    }
}